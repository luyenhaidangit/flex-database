CREATE TABLE "SubAccounts"
(
    ID               NUMBER(19) GENERATED BY DEFAULT ON NULL AS IDENTITY
        CONSTRAINT "PK_SubAccounts" PRIMARY KEY,
    INVESTOR_ID      NUMBER(19) NOT NULL, -- Liên kết với bảng Investors nhưng không có khóa ngoại
    ACCOUNT_NO       VARCHAR2(50) NOT NULL UNIQUE, -- Mã số tiểu khoản (duy nhất)
    ACCOUNT_TYPE     VARCHAR2(50) NOT NULL, -- Loại tài khoản: CASH, MARGIN, DERIVATIVES, BONDS
    BALANCE          NUMBER(19, 4) DEFAULT 0 NOT NULL, -- Số dư tài khoản
    STATUS           VARCHAR2(20) DEFAULT 'ACTIVE' NOT NULL, -- ACTIVE, BLOCKED, CLOSED
    CREATEDDATE      TIMESTAMP(7) WITH TIME ZONE NOT null,
    LASTMODIFIEDDATE TIMESTAMP(7) WITH TIME ZONE
);
/
