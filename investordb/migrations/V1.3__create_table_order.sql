CREATE TABLE Orders (
    ID               NUMBER(19) GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    INVESTOR_ID      NUMBER(19) NOT NULL,
    SUB_ACCOUNT_ID   NUMBER(19) NOT NULL,
    ORDER_TYPE       VARCHAR2(50) NOT NULL, -- BUY, SELL, LIMIT, MARKET
    QUANTITY         NUMBER(19, 4) NOT NULL,
    PRICE            NUMBER(19, 4),
    STATUS           VARCHAR2(20) DEFAULT 'PENDING' NOT NULL, -- PENDING, EXECUTED, CANCELED
    CREATEDDATE      TIMESTAMP(7) WITH TIME ZONE NOT null,
    LASTMODIFIEDDATE TIMESTAMP(7) WITH TIME ZONE
);
/
