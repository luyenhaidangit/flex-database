CREATE TABLE "Branches" (
    ID                   NUMBER(19) GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    CODE                 VARCHAR2(50) NOT NULL,
    NAME                 NVARCHAR2(200) NOT NULL,
    ADDRESS              NVARCHAR2(500),
    REGION               NVARCHAR2(100),
    MANAGER              NVARCHAR2(100),
    ESTABLISHED_DATE     DATE,
    STATUS               VARCHAR2(20) NOT NULL,
    CREATEDDATE          TIMESTAMP(7) WITH TIME ZONE NOT NULL,
    CREATEDBY            VARCHAR2(255),
    LASTMODIFIEDDATE     TIMESTAMP(7) WITH TIME ZONE,
    LASTMODIFIEDBY       VARCHAR2(255)
);

CREATE TABLE "BranchHistories" (
    ID                   NUMBER(19) GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    BRANCH_ID            NUMBER(19) NOT NULL,
    CHANGE_TYPE          VARCHAR2(50),
    OLD_DATA             CLOB,
    NEW_DATA             CLOB,
    COMMENT              NVARCHAR2(1000),
    CREATEDDATE          TIMESTAMP(7) WITH TIME ZONE NOT NULL,
    CREATEDBY            VARCHAR2(255),
    LASTMODIFIEDDATE     TIMESTAMP(7) WITH TIME ZONE,
    LASTMODIFIEDBY       VARCHAR2(255)
);

CREATE TABLE "BranchRequests" (
    ID                   NUMBER(19) GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    BRANCH_ID            NUMBER(19),
    REQUEST_TYPE         VARCHAR2(50) NOT NULL,
    PROPOSED_DATA        CLOB NOT NULL,
    REASON               NVARCHAR2(1000),
    STATUS               VARCHAR2(20) NOT NULL,
    APPROVED_BY          NUMBER(19),
    APPROVED_DATE        DATE,
    APPROVAL_COMMENT     NVARCHAR2(1000),
    REQUESTED_BY         VARCHAR2(255),
    CREATEDDATE          TIMESTAMP(7) WITH TIME ZONE NOT NULL,
    CREATEDBY            VARCHAR2(255),
    LASTMODIFIEDDATE     TIMESTAMP(7) WITH TIME ZONE,
    LASTMODIFIEDBY       VARCHAR2(255)
);
